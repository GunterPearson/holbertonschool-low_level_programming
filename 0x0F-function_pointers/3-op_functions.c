#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"
/**
 * op_add - start of funtion
 * @a: first integer given
 * @b: second integer given
 *
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - start of funtion
 * @a: first integer given
 * @b: second integer given
 *
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - start of funtion
 * @a: first integer given
 * @b: second integer given
 *
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - start of funtion
 * @a: first integer given
 * @b: second integer given
 *
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - start of funtion
 * @a: first integer given
 * @b: second integer given
 *
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
